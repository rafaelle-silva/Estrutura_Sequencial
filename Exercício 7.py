# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
lado = int(input('Usuário, informe o lado do quadrado para o cálculo da área: '))
print(f'Usuário, a área do quadrado é: {(lado**2)} cm²')
print(f'Usuário, o dobro desta área é: {(lado**2) + (lado**2)} cm²')
